    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        int baris = table.getRowCount();
        int biaya = 0;
        int jml, hrg;
        TableModel tabelModel;
        tabelModel = table.getModel();
        
        for (int i = 0; i < baris; i++) {
            jml = Integer.parseInt(tabelModel.getValueAt(i, 2).toString());
            hrg = Integer.parseInt(tabelModel.getValueAt(i, 3).toString());
            biaya = biaya + (jml * hrg);
        }
        if (biaya > 200000) {
            int discount = biaya * 10 /100;
            bla.setText(String.valueOf(biaya - discount));
        } else
            bla.setText(String.valueOf(biaya));
    }//GEN-LAST:event_totalActionPerformed


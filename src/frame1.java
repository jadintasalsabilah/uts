    private void selectData() {
        String kolom[] = {"no_pembelian","pesanan","jumlah","harga"};
        DefaultTableModel dtm  = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = db.executeQuery(SQL);
        
        try {
            while (rs.next()) {
                String no_pembelian = rs.getString(1);
                String pesanan= rs.getString(2);
                String jumlah = rs.getString(3);
                String harga = rs.getString(4);
                String data[] = {no_pembelian,pesanan,jumlah,harga,};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frame1.class.getName()).log(Level.SEVERE, null, ex);
        }
        table.setModel(dtm);
    }

